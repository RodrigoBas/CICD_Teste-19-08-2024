function add(a, b) {
    let soma = a+b;
    return soma
}

function subtract (a, b){
    let diferenca = a-b;
    return diferenca
}

function multiply (a, b){
    let produto = a*b;
    return produto
}

function divide (a, b){
    let divisao = a/b;
    return divisao
}

module.exports = { add, subtract, multiply, divide } ;
